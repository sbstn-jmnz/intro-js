// The operator typeof reveals the type

var num = 1
console.log(typeof(num))
var decimal = 1.23
console.log(typeof(decimal))
var exp = 2e+4;
console.log(typeof(exp))

var str = "Some long weird string"
console.log(typeof(str))
var str2 = '1'
console.log(typeof(str2));

var sumString = str + str2

console.log(sumString);

var someObject = {
  'name': "Sebas",
  'age': null
}

console.log(typeof(someObject));